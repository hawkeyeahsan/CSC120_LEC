//=================================================================================================
public class Cop {
    //-------------------------------------------------------------------------------------------------
    private String name;
    private Gun bigGun;
    //-------------------------------------------------------------------------------------------------
    public Cop() {

        name = null;
        bigGun = null;
    }
    //-------------------------------------------------------------------------------------------------
    public Cop(String name) {

        this();
        this.name = name;
    }
    //-------------------------------------------------------------------------------------------------
    public String toString() {

        String displayString;

        displayString = "Officer " + name;
        if (bigGun != null) {
            displayString += " has a " + bigGun;
        } else {
            displayString += " has no gun";
        }
        return(displayString);
    }
    //-------------------------------------------------------------------------------------------------
    public void setGun(Gun newGun) {

        bigGun = newGun;
    }
    //-------------------------------------------------------------------------------------------------
    public void loadGun() {

        if (bigGun != null) {
            bigGun.load();
        }
    }
    //-------------------------------------------------------------------------------------------------
    public void fireGun() {

        if (bigGun != null) {
            bigGun.fire();
        }
    }
//-------------------------------------------------------------------------------------------------
}
//=================================================================================================
